# sue trunc(), floor(), and ceil() for numbers
import math
a = -2.8
b = -0.5
c = 0.2
d = 1.5
e = 2.9

print("trunc ")
print(math.trunc(a))
print(math.trunc(b))
print(math.trunc(c))
print(math.trunc(d))
print(math.trunc(e))

print("floor ")
print(math.floor(a))
print(math.floor(b))
print(math.floor(c))
print(math.floor(d))
print(math.floor(e))

print("ceil ")
print(math.ceil(a))
print(math.ceil(b))
print(math.ceil(c))
print(math.ceil(d))
print(math.ceil(e))